        <section id="cercle">
            <h2>Le cercle d'or</h2>
            <p>Le cercle d'or est une notion élaborée par Simon Sinek, auteur et conférencier. Ce cercle comporte 3 couches : <strong>Quoi</strong>, <strong>Comment</strong>, <strong>Pourquoi</strong>.</p>
            <p>La plupart des entreprises dans leur marketing, ou la plupart des gens, parlent de leur <strong>Quoi</strong>. Qu'est-ce que tu fais ? Je fais ça.</p>
            <p>Simon Sinek a étudié les entreprises comme Apple et les grands communicants comme Martin Luther King pour comprendre ce qui les différenciait. Ils ne commencent pas par leur <strong>Quoi</strong> mais par leur <strong>Pourquoi</strong> !</p>
            <p>Ainsi, en commençant par le <strong>Pourquoi</strong>, on explique la raison qui nous pousse à faire ce que l'on fait, pourquoi on a choisi telle ou telle voie. C'est notre raison d'être. On devient inspirant.</p>
            <p>La théorie du cercle d'or repose en fait sur un simple mot : l'<strong>authenticité</strong>. Raconter son histoire, sa raison d'être, qui on est, ce qu'on fait et pourquoi on le fait.</p>
