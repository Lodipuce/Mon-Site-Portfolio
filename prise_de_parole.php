                    <section id="detendre">
                        <h2>Se détendre et sourire !</h2>
                        <p>Quelqu'un qui est trop tendu risque d'oublier de dire des choses, de bafouiller, etc</p>
                        <p>La première chose à faire avant de prendre la parole est de se détendre au maximum : respiration profonde, humour, méditation...</p>
                        <p>Ensuite, lors de la prise de parole en elle-même, essayez de penser à ceci :</p>
                        <ul>
                            <li>Le regard : evitez de trop regarder votre préparation. Il faut regarder les auditeurs pour qu'ils se sentent impliqués. Vous pourrez même trouver quelques regrds bienveillants.</li>
                            <li>La posture : se mettre face au public, bien campé sur ses deux jambes, éviter de trop bouger et à l'inverse d'être trop statique.</li>
                            <li>La respiration : quand on est stressé, on a tendance à débiter ce que l'on a à dire et parfois à oublier de respirer ! Donc il faut penser à faire des pauses et à respirer !</li>
                            <li>La voix : poser sa voix, articuler.</li>
                        </ul>

