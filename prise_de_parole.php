                    <section id="capter">
                        <h2>Capter l'attention du public</h2>
                        <p>L'idéal est de capter l'attention du public dès la première minute de la présentation. Il existe différentes techniques pour cela :</p>
                        <ul>
                            <li>Commencer par les éléments forts de sa présentation, voire même par une question rhétorique qui permet d'enchaîner sur ce qu'on a à dire.</li>
                            <li>Commencer par une citation. Dire ce qu'on en pense, demander aux auditeurs également.</li>
                            <li>Parler d'une voix dynamique, claire et forte ! Cela peut surprendre et en réveiller certains !</li>
                            <li>Raconter une histoire, une anecdote.</li>
                        </ul>

                        <p>Vous avez réussi à capter, l'attention du public. Félicitations !</p>
                        <p>Maintenant il ne faut pas la perdre.</p>
                        <p>Comme dit plus haut, pour éviter de perdre les auditeurs, il faut utiliser des termes clairs, concis et cohérents. <br>De plus, Pour maintenir l'attention, il faut du rythme, de l'enthousiasme et garder un contact visuel avec les personnes. Sans oublier de garder le sourire !</p>
                    
