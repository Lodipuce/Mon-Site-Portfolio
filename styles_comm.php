                    <section id="direct">
                        <h2>Style direct</h2>
                        <p>Ce style est décisif, compétitif, indépendant et sûr de lui. Parce que les personnes ayant un style de communication direct sont dédiées, concentrées sur les résultats, ambitieuses, tournées vers un objectif et motivées, les autres peuvent les percevoir comme étant volontaires ou exigeantes. Elles peuvent être considérées comme impatientes lorsqu'elles sont lasses. Elles aiment être aux manettes et peuvent se sentir frustrées si elles dépendent des autres. Elles mesurent le progrès en fonction des réussites et des succès et sont motivées par les défis. Au travail, elles témoignent de plus d'attention aux résultats qu'aux relations et expriment difficilement leurs sentiments. Leur rythme est rapide et décidé et elles aiment les environnements animés, efficaces, structurés et formels</p>
                    </section>

                    <section id="initiateur">
                        <h2>Style initiateur</h2>
                        <p>Ce style est sociable, enthousiaste, énergique, spontané et enjoué. En raison de la nature sociale des personnes ayant un style de communication initiateur, elles peuvent être vues comme des personnes qui parlent plus qu'elles n'écoutent. Elles sont souvent perçues comme étant sûres d'elles, innovantes et persuasives. Elles aiment se sentir acceptées et sont motivées par les liens sociaux. Elles réagissent fortement aux encouragements et aux approbations. Leur rythme est rapide et elles peuvent sembler parfois impulsives. Elles préfèrent un environnement de travail stimulant, personnel et amical.</p>
                    </section>

                    <section id="encourageant">
                        <h2>Style encourageant</h2>
                        <p>Ce style est calme, constant, abordable, sincère et doux. Comme les personnes qui ont un style de communication encourageant n'aiment pas les changements, elles peuvent sembler indécises. Le plus souvent, elles sont perçues comme étant attentives, patientes et aimables. En raison de leurs capacités d'écoute active, les autres les considèrent comme coopératives, fiables et fidèles. Elles sont souvent modestes et préfèrent être encouragées en privé. Patientes et suivant un rythme lent, elles aiment les environnements personnels, détendus et sans tension. Elles mettent l'accent sur les relations proches et n'aiment pas les conflits, mais peuvent jouer le rôle de médiateur si nécessaire.</p>
                    </section>

                    <section id="analytique">
                        <h2>Style analytique</h2>
                        <p>Ce style est précis, exact, analytique et logique. Les personnes ayant un style de communication analytique sont systématiques et sont tournées vers leurs tâches. Elles sont parfois perçues comme étant perfectionnistes. Elles sont organisées, autonomes, tournées vers les résultats et diplomates. Elles sont motivées par la certitude et donnent rarement leur opinion si on ne la leur demande pas. Elles sont lentes et prudentes dans leur rythme et aiment les environnements structurés, ordonnés et fonctionnels. Comme elles ont besoin d'être sûres de leur position et des attentes des autres, elles gardent leurs informations privées et n'expriment pas facilement leurs émotions.</p>
                    </section>

